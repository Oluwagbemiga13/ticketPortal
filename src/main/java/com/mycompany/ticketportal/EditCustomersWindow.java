    public void fillTable(){

    for(int i = 0; i< privateCustomerArrayList.size(); i++){
        
        PrivateCustomer p = privateCustomerArrayList.get(i);
        
        String firstName  = p.privateFirstName;
        String lastName = p.privateLastName;
        int position = p.position;
        String idCustomer = String.valueOf(position);
        String login = p.login;
        
        ticketsTable.setValueAt(firstName, i, 0);
        ticketsTable.setValueAt(lastName, i, 1);
        ticketsTable.setValueAt(idCustomer, i, 2);
        ticketsTable.setValueAt(login, i, 3);
        
    }
    }
