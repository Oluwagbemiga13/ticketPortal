    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        // TODO add your handling code here:

        origin = (String) originComboBox.getSelectedItem();
        destination = (String) destinationComboBox.getSelectedItem();
        amountString = (String) amountComboBox.getSelectedItem();
        
        if(!sameDestination(origin,destination)){
            int amount = Integer.parseInt(amountString);
            int price;
        
            if("Pardubice".equals(destination)){
                price = 500;
            }
            else{
                price = 600;
            }
            for(int i = 0; i < amount; i++){
                createTicket(origin, destination, price);  
            }
            errorLabel.setText(origin + "->" + destination + " was created " + amount + "x.");
            errorLabel.setForeground(java.awt.Color.green);
            errorLabel.setVisible(true);
        }
        else{
            errorLabel.setVisible(true);
            errorLabel.setText("Origin and destination cant be the same");
            errorLabel.setForeground(java.awt.Color.red);
        }
    }//GEN-LAST:event_okButtonActionPerformed

